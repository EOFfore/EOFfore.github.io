public class Movie implements Cloneable{
    private String name;
    private String type;
    private String duration;
    private String prodution;
    private String showDate;
    private String poster;
    private String brief;
    private String director;
    private String actor;
    private String res;
    private double mark;
    private String showDate_country;





    public String getShowDate_country() {
        return showDate_country;
    }


    public void setShowDate_country(String showDate_country) {
        this.showDate_country = showDate_country;
    }

    public String getRes() {
        return res;
    }

    public void setRes(String res) {
        this.res = res;
    }

    public double getMark() {
        return mark;
    }

    public void setMark(double mark) {
        this.mark = mark;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getProdution() {
        return prodution;
    }

    public void setProdution(String prodution) {
        this.prodution = prodution;
    }

    public String getShowDate() {
        return showDate;
    }

    public void setShowDate(String showDate) {
        this.showDate = showDate;
    }

    public String getPoster() {
        return poster;
    }

    public void setPoster(String poster) {
        this.poster = poster;
    }

    public String getBrief() {
        return brief;
    }

    public void setBrief(String brief) {
        this.brief = brief;
    }

    public String getDirector() {
        return director;
    }

    public void setDirector(String director) {
        this.director = director;
    }

    public String getActor() {
        return actor;
    }

    public void setActor(String actor) {
        this.actor = actor;
    }

}
